#include "libft.h"

void	ft_bzero(void *ptr, size_t n)
{
	char	*tmp_ptr;
	size_t	i;

	i = 0;
	tmp_ptr = (char *) ptr;
	while (i < n)
		tmp_ptr[i++] = 0;
}
